import fs from 'fs';
import path from 'path';
import { repoRoot } from './_shared/runtime.mjs';

const openapiPath = path.resolve(repoRoot, 'backend/docs/openapi.yaml');
const outputPath = path.resolve(repoRoot, 'apps/desktop/generated/openapi-types.ts');
const source = fs.readFileSync(openapiPath, 'utf8');
const lines = source.split(/\r?\n/);

function parsePathOperations() {
  const operations = [];
  let currentPath = '';
  for (const rawLine of lines) {
    const pathMatch = rawLine.match(/^  (\/[^:]+):\s*$/);
    if (pathMatch) {
      currentPath = pathMatch[1];
      continue;
    }
    const methodMatch = rawLine.match(/^    (get|post|put|patch|delete):\s*$/);
    if (currentPath && methodMatch) {
      operations.push({ path: currentPath, method: methodMatch[1].toUpperCase() });
    }
  }
  return operations;
}

function parseSchemaNames() {
  const start = lines.findIndex((line) => line === 'components:');
  if (start === -1) {
    throw new Error('OpenAPI components section not found');
  }
  const schemasLine = lines.findIndex((line, index) => index > start && line === '  schemas:');
  if (schemasLine === -1) {
    throw new Error('OpenAPI components.schemas section not found');
  }

  const names = [];
  for (let i = schemasLine + 1; i < lines.length; i++) {
    const line = lines[i];
    if (/^  [A-Za-z0-9_-]+:/.test(line)) break;
    const match = line.match(/^    ([A-Za-z][A-Za-z0-9]*):\s*$/);
    if (match) names.push(match[1]);
  }
  return names;
}

function toOperationKey(operation) {
  const suffix = operation.path
    .replace(/^\//, '')
    .replace(/\{([^}]+)\}/g, 'by_$1')
    .replace(/[^A-Za-z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '')
    .toLowerCase();
  return `${operation.method.toLowerCase()}_${suffix}`;
}

function generateTypes() {
  const operations = parsePathOperations();
  const schemas = parseSchemaNames();
  if (operations.length === 0) {
    throw new Error('No OpenAPI paths found');
  }
  if (schemas.length === 0) {
    throw new Error('No OpenAPI schemas found');
  }

  const operationEntries = operations
    .map((operation) => `  '${toOperationKey(operation)}': { method: '${operation.method}'; path: '${operation.path}' };`)
    .join('\n');
  const schemaEntries = schemas
    .map((name) => `  ${name}: Record<string, unknown>;`)
    .join('\n');
  const methods = Array.from(new Set(operations.map((operation) => operation.method))).sort();

  return `// Auto-generated by scripts/generate-openapi-types.mjs. Do not edit manually.\n` +
    `// Source: backend/docs/openapi.yaml\n\n` +
    `export type OpenApiMethod = ${methods.map((method) => `'${method}'`).join(' | ')};\n\n` +
    `export interface OpenApiOperationMap {\n${operationEntries}\n}\n\n` +
    `export interface OpenApiSchemas {\n${schemaEntries}\n}\n\n` +
    `export type OpenApiPath = OpenApiOperationMap[keyof OpenApiOperationMap]['path'];\n` +
    `export type OpenApiSchemaName = keyof OpenApiSchemas;\n`;
}

const generated = generateTypes();
const current = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, 'utf8') : '';
const checkMode = process.argv.includes('--check');

if (checkMode) {
  if (current !== generated) {
    console.error(`Generated OpenAPI types are out of date: ${path.relative(repoRoot, outputPath)}`);
    console.error('Run: pnpm generate:api-types');
    process.exit(1);
  }
  console.log(`OpenAPI types are up to date: ${path.relative(repoRoot, outputPath)}`);
} else {
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, generated, 'utf8');
  console.log(`Generated ${path.relative(repoRoot, outputPath)} from ${path.relative(repoRoot, openapiPath)}`);
}
